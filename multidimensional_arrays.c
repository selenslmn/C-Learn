int a[3][4][2];

// Here the actual array has 3 elements. But every element of this 3-element array is actually a matrix. a is of type "int [3][4][2]".
// Elements of a are of type "int[4][2]". The elements of the array, which is this element, are of type "int[2]".

/*----------------------------------------------------------------------------------------------------------------------------------------------------*/

#include <stdio.h>

int main(void)
{
	int a[3][2];

	a[0][0] = 10;
	a[0][1] = 20;
	a[1][0] = 30;
	a[1][1] = 40;
	a[2][0] = 50;
	a[2][1] = 60;

	for (int i = 0; i < 3; ++i) {
		for (int k = 0; k < 2; ++k)
			printf("%d ", a[i][k]);
		printf("\n");
	}

	return 0;
}

